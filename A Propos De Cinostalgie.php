<h1>Cinostalgie</h1>

<p>Description du projet :</p>

<p>C’est un site internet qui distribue et exploite les œuvres cinématographiques des débuts du cinéma; revoir ses films préférés étant enfant/adolescent (ou même adulte) c’est satisfaire sa faim nostalgique!</p>

<h2>Raison de le faire :</h2>

<p>étant une personne fascinée par les films des années 90’s et qui sait très bien la difficulté de trouver un de ces derniers en ligne, je voudrais créer une plateforme qui facilite la recherche des fans de la vieille époque pour qu’ils puissent profiter de leurs films préfèrés.</p>

